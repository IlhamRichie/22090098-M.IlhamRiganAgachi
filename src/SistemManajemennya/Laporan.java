    private void PieChartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PieChartActionPerformed
        // TODO add your handling code here:

        DefaultPieDataset dataset = new DefaultPieDataset();

        try {
            Connection con= DBConnection.connection();

            PreparedStatement ps = con.prepareStatement("select C.CID, C.CatName, count(OP.OID) as CatSold from OrderProduct as OP inner join Products as P on OP.PID = P.PID inner join Category as C on P.CID = C.CID group by C.CID, C.CatName");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                dataset.setValue(rs.getString("CatName"), rs.getInt("CatSold"));
            }

            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Laporan.class.getName()).log(Level.SEVERE, null, ex);
        }

        JFreeChart pieChart = ChartFactory.createPieChart("Category Sales", dataset, true, true, true);
        PiePlot Pie = (PiePlot) pieChart.getPlot();
        ChartFrame pieFrame = new ChartFrame("Category Sales - Pie Chart",pieChart);
        pieFrame.setVisible(true);
        pieFrame.setSize(500, 500);

    }//GEN-LAST:event_PieChartActionPerformed

    private void BarChartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BarChartActionPerformed
        // TODO add your handling code here:

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        try {
            Connection con= DBConnection.connection();

            PreparedStatement ps = con.prepareStatement("select sum(OrdTotalAmount) as DateTotal, convert(date, OrdDate) as ODate from Orders group by convert(date, OrdDate)");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                dataset.setValue(rs.getInt("DateTotal"), "Total Sales", rs.getDate("ODate"));
            }

            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Laporan.class.getName()).log(Level.SEVERE, null, ex);
        }

        JFreeChart barChart = ChartFactory.createBarChart3D("Daily Sales", "Date", "Total Sales", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot bar = barChart.getCategoryPlot();
        bar.setRangeGridlinePaint(Color.black);
        ChartFrame barFrame = new ChartFrame("Daily Sales - Bar Chart", barChart);
        barFrame.setVisible(true);
        barFrame.setSize(500, 700);

    }//GEN-LAST:event_BarChartActionPerformed

