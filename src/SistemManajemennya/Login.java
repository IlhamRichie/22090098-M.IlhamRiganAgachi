    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
//       String rs2 = "select use from Employee where userName=? and password=? " ; 
    try {

              Connection conn= DBConnection.connection();
            
             PreparedStatement st = conn.prepareStatement("select EID, EmpPassword, AID from Employee");
            
            ResultSet result = st.executeQuery();
            
            boolean flag = false;
            while (result.next()){
                int eid= result.getInt("EID");
                int id = Integer.parseInt(usernameField.getText());
                String pass = new String (passwordField.getPassword());
                String epass = result.getString("EmpPassword");
                int aid= result.getInt("AID");

                if (eid == id && pass.equals(epass)) {
                    if (aid == 0){
                        setVisible(false);
                        new Admin().setVisible(true);
                    } else  {
                        setVisible(false);
                        new Kasir().setVisible(true); }
                    flag = true;
                    break;
                  } 
                }
            if (!flag){
                    JOptionPane.showMessageDialog(this,"Incorrect username or password","Invalid Login",JOptionPane.INFORMATION_MESSAGE);
                    usernameField.setText("");
                    passwordField.setText("");
            }
            
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
      }
        
        
    }//GEN-LAST:event_LoginActionPerformed

    
    
